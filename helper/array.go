package helper

import "github.com/gusanmaz/algorithms/types/comparables"

func Swap(a []comparables.Interface, i, j int){
	a[i], a[j] = a[j], a[i]
}

func Comp(a []comparables.Interface, i, j int) int{
	if a[i].EqualTo(a[j]){
		return 0
	}else if a[i].LessThan(a[j]){
		return -1
	}else{
		return 1
	}
}


